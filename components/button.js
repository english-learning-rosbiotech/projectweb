// ===== Button Component =====

export function createButton(text, variant = 'primary', onClick = null, icon = null) {
  const button = document.createElement('button');
  button.className = `btn btn--${variant}`;
  
  if (icon) {
    const iconSpan = document.createElement('span');
    iconSpan.innerHTML = icon;
    button.appendChild(iconSpan);
  }
  
  const textSpan = document.createElement('span');
  textSpan.textContent = text;
  button.appendChild(textSpan);
  
  if (onClick) {
    button.addEventListener('click', onClick);
  }
  
  return button;
}

export function createIconButton(icon, variant = 'primary', onClick = null) {
  const button = document.createElement('button');
  button.className = `btn btn--${variant} btn--icon`;
  button.innerHTML = icon;
  
  if (onClick) {
    button.addEventListener('click', onClick);
  }
  
  return button;
}

export function createMenuButton(title, icon, colorClass, onClick) {
  const button = document.createElement('div');
  button.className = 'menu-item';
  
  const iconDiv = document.createElement('div');
  iconDiv.className = `menu-item__icon menu-item__icon--${colorClass}`;
  iconDiv.textContent = icon;
  
  const titleDiv = document.createElement('div');
  titleDiv.className = 'menu-item__title';
  titleDiv.textContent = title;
  
  button.appendChild(iconDiv);
  button.appendChild(titleDiv);
  
  button.addEventListener('click', onClick);
  
  return button;
}

